import { app, BrowserWindow, dialog, Menu, shell } from 'electron'
import { store, vxm } from '../store'

export default class ElectronMenu {
  win: BrowserWindow
  isMac = process.platform === 'darwin'

  template: object[] = [
    // { role: 'appMenu' }
    ...(this.isMac
      ? [
          {
            label: app.getName(),
            submenu: [
              { role: 'about' },
              { type: 'separator' },
              { role: 'services' },
              { type: 'separator' },
              { role: 'hide' },
              { role: 'hideothers' },
              { role: 'unhide' },
              { type: 'separator' },
              { role: 'quit' },
            ],
          },
        ]
      : []),
    // { role: 'fileMenu' }
    {
      label: 'File',
      submenu: [
        {
          label: 'Open folder',
          click: () => {
            this.openFolder()
          },
          accelerator: 'CommandOrControl+O',
        },
        this.isMac ? { role: 'close' } : { role: 'quit' },
      ],
    },
    // { role: 'editMenu' }
    {
      label: 'Edit',
      submenu: [
        { role: 'undo' },
        { role: 'redo' },
        { type: 'separator' },
        { role: 'cut' },
        { role: 'copy' },
        { role: 'paste' },
        ...(this.isMac
          ? [
              { role: 'pasteAndMatchStyle' },
              { role: 'delete' },
              { role: 'selectAll' },
              { type: 'separator' },
              {
                label: 'Speech',
                submenu: [{ role: 'startspeaking' }, { role: 'stopspeaking' }],
              },
            ]
          : [{ role: 'delete' }, { type: 'separator' }, { role: 'selectAll' }]),
      ],
    },
    // { role: 'viewMenu' }
    {
      label: 'View',
      submenu: [
        { role: 'reload' },
        { role: 'forcereload' },
        { role: 'toggledevtools' },
        { type: 'separator' },
        { role: 'resetzoom' },
        { role: 'zoomin' },
        { role: 'zoomout' },
        { type: 'separator' },
        { role: 'togglefullscreen' },
      ],
    },
    // { role: 'windowMenu' }
    {
      label: 'Window',
      submenu: [
        { role: 'minimize' },
        { role: 'zoom' },
        ...(this.isMac
          ? [
              { type: 'separator' },
              { role: 'front' },
              { type: 'separator' },
              { role: 'window' },
            ]
          : [{ role: 'close' }]),
      ],
    },
    {
      role: 'help',
      submenu: [
        {
          label: 'Learn More',
          click() {
            shell.openExternalSync('https://electronjs.org')
          },
        },
      ],
    },
  ]

  constructor(win: BrowserWindow) {
    this.win = win
    const menu = Menu.buildFromTemplate(this.template)
    Menu.setApplicationMenu(menu)
  }

  openFolder() {
    const folderName = dialog.showOpenDialog(this.win, {
      properties: ['openDirectory'],
    })

    if (folderName) {
      vxm.graph.importTerraformFolder(folderName[0])
    }
  }
}
